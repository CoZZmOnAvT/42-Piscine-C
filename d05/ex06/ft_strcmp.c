int		ft_strcmp(char *s1, char *s2)
{
	int		it;

	it = 0;
	while (s1[it] > 0 || s2[it] > 0)
	{
		if (s1[it] != s2[it])
			return ((unsigned int)s1[it] - (unsigned int)s2[it]);
		it++;
	}
	return (0);
}
