char	*ft_strlowcase(char *str)
{
	int		it;

	it = 0;
	while (str[it] > 0)
	{
		if (str[it] >= 'A' && str[it] <= 'Z')
			str[it] = str[it] + 32;
		it++;
	}
	return (str);
}
