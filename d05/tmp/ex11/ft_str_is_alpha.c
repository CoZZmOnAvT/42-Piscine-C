int		ft_str_is_alpha(char *str)
{
	int		it;

	it = 0;
	while (str[it])
	{
		if (str[it] < 'A' || (str[it] > 'Z' && str[it] < 'a') || str[it] > 'z')
			return (0);
		it++;
	}
	return (1);
}
