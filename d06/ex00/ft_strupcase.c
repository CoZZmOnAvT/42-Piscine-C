char	*ft_strupcase(char *str)
{
	int		it;

	it = 0;
	while (str[it] > 0)
	{
		if (str[it] >= 'a' && str[it] <= 'z')
			str[it] = str[it] - 32;
		it++;
	}
	return (str);
}
