int		ft_count_if(char **tab, unsigned long (*f)(char*))
{
	int		it;
	int		len;

	it = 0;
	len = 0;
	while (tab[it])
	{
		if (f(tab[it]) == 1)
			len++;
		it++;
	}
	return (len);
}
